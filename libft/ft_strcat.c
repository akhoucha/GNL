#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i1;
	int j2;

	i1 = 0;
	j2 = 0;
	while (s1[i1])
		i1++;
	while (s2[j2])
	{
		s1[i1] = s2[j2];
		i1++;
		j2++;
	}
	s1[i1] = '\0';
	return (s1);
}
